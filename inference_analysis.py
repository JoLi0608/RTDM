def testenv(cpus, gpu, alg, env, dic = inf_dict):
    res = {}
    for exp, data in dic.items():
        # print(exp,data)
        ave_inf = sum(data)/len(data)
        exp = exp.split("_")
        # print((exp[1] == cpu), (exp[2] == alg and exp[-1] == gpu))
        if exp[1] in cpus and exp[2] == alg and exp[-1] == gpu and exp[3] == env:
            cpu = exp[1]
            res[cpu] = ave_inf
    # if alg == 'pets':
    #     res['Humanoid-v2'] = 0
    return res 
